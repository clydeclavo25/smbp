          </li>
          <li>
            For every ten (10) student-participants per school delegation, one (1) coach/adviser is free from registration fee. If it is less than ten (10) student-participants, the coach/adviser should pay the registration in full. All Regional Supervisors in Science and Mathematics who wish to attend will be treated as guests and therefore free of registration.   
          </li>
          <li>
            A Paper-and-Pencil Test (PPT)  is to be provided for quiz participants. A multiple choice, fifty (50) items test shall be prepared. This is a one-hour test. Answers will be written on the test paper. All scratch papers will be submitted after the test.
          </li>
          <li>
            In case of a tie, clincher questions will be given. It will be a knockout system between or among the 
            contestants with equal scores until a winner emerges. The first one who answers correctly will be declared 
            the winner.
          </li>
          <li>
          A medallion and a certificate shall be awarded to the winners of the different quiz events. Top five (5) winners in the quiz per grade level will be recognized. The 1st placer will receive a gold medallion. 2nd placer will receive a silver medallion. The 3rd placer will receive a bronze medallion the 4th and 5th placers will be given a Certificate of Recognition. All student-participants will receive a certificate of merit. The coach/adviser will also receive a <b>certificate of recognition.</b> An over-all standing of Schools Division that participated shall be determined. Trophies and certificate of recognition shall be awarded to top three (3) Schools Division Offices (SDOs).
          </li>
          <li>
            All participants and guests must abide with the house rules within the venue and in the entire duration of the contest. Students and coaches/advisers must abide with the contests rules and regulations.
          </li>
          <li>
            The decision of judges in all contests is final and irrevocable
          </li>
        </ol>
