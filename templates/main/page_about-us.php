<section id="the-challenge">
      <div class="container">
        <div class="center wow fadeInDown">
          <h2><i class="fa fa-thumbs-up" aria-hidden="true"></i> The Challenge</h2>
          <p class="lead text-justify">
            It is an undeniable truth that both Science and Mathematics, being the core subjects in the present education curriculum, have strongly dictate and influence the academic future of learners. The deep knowledge or mastery of the subject's acts like a magnetic compass providing direction to a productive career path for students. One strong statement drawn from a series of consultation meetings and focus group discussions with different sectors of society held in 2006 during the crafting of Science Education Framework, says that <em>“school science education should support the development of scientific literacy in all students as well as motivate them to pursue careers in science, technology, and engineering.”</em> On the same weight, as mentioned by Pascua (1993), the goals of Mathematics Education at the basic education level is <em>“to provide opportunities for individuals to develop skills and attitudes needed for effective participation in everyday living and prepare them for further education and the world of work so that they make worthwhile contributions to the society at large."</em>
          </p>
          <p class="lead text-justify">
            To realize and to make education meaningful to all Filipino children, both Science and Mathematics as important curricular subjects must be strengthened. Through the years, the curriculum for Science and Mathematics in the Philippines have been further strengthened to attune it to the changing environment and keep up with the latest developments in the field to enable our children to participate actively and effectively in modern society. It is an accepted fact that there are challenges specifically among our learners and the school in general when it comes to assessments and academic competition in Science and Mathematics which, in some aspects, were used as barometers to measure academic performance. Our students gained recognition for their accomplishments in the International Science and Engineering Fair, Robotics Competition, and Physics Olympiad, to name a few. There are also reports of students in far-flung rural schools scoring much higher than the international mean in the case of the Third/Trends in International Mathematics and Science Study (TIMSS) or have gone beyond the 75% mastery level in the case of the National Achievement Test (NAT). However, the accomplishments of a few students are overshadowed by the consistently poor performance of Filipino students in the national and international assessment studies. 
          </p>
          <p class="lead text-justify">
            The results of studies conducted by the University of the Philippines National Institute for Science and Mathematics Education Development (UP NISMED) in 2004 & 2005 reveal that Filipino students have low retention of concepts, have limited reasoning, analytical skills and poor communication skills. In addition, a large percentage of Grades 6 and 10 students in selected schools cannot apply concepts to real-life problem-solving situations nor design an investigation to solve a problem. Realizing the unfavorable situations besetting Science and Mathematics education in the country today, effective remedies should be applied. The solutions everybody is searching for do not emanate and solely controlled by the government or by the Department of Education. Solutions are actually the compilation of the best practices in the field of education, bright ideas, outstanding activities such as trainings, seminars and sound academic competition provided by different sectors of the community. 
          </p>

